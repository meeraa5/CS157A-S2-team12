
public class Product {
	private int productid;
	private String productName;
	private String productDescription;
	private float price;
	private String productCondition;
	private int quantityAvailable;
	private String productStatus;
	private int dateAdded;
	private int lowStockNotice;
	private int categoryID;
	private int createdbyAdminID;
	
	public int getProductid() {
		return productid;
	}
	public void setProductid(int productid) {
		this.productid = productid;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getProductDescription() {
		return productDescription;
	}
	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public String getProductCondition() {
		return productCondition;
	}
	public void setProductCondition(String productCondition) {
		this.productCondition = productCondition;
	}
	public int getQuantityAvailable() {
		return quantityAvailable;
	}
	public void setQuantityAvailable(int quantityAvailable) {
		this.quantityAvailable = quantityAvailable;
	}
	public String getProductStatus() {
		return productStatus;
	}
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	public int getDateAdded() {
		return dateAdded;
	}
	public void setDateAdded(int dateAdded) {
		this.dateAdded = dateAdded;
	}
	public int getLowStockNotice() {
		return lowStockNotice;
	}
	public void setLowStockNotice(int lowStockNotice) {
		this.lowStockNotice = lowStockNotice;
	}
	public int getCategoryID() {
		return categoryID;
	}
	public void setCategoryID(int categoryID) {
		this.categoryID = categoryID;
	}
	public int getCreatedbyAdminID() {
		return createdbyAdminID;
	}
	public void setCreatedbyAdminID(int createdbyAdminID) {
		this.createdbyAdminID = createdbyAdminID;
	}
	
}
